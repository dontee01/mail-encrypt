    private void jBtSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtSendActionPerformed
        // TODO add your handling code here:
        
		if (!validateFields()) {
			return;
		}
		
		String toAddress = jTxtTo.getText();
		String subject = jTxtSubj.getText();
		String message = jTextAreaMsg.getText();
                
		try {
                String enctext = encryptUtil.Encrypt(message);
//                JDialog.setDefaultLookAndFeelDecorated(true);
    int response = JOptionPane.showConfirmDialog(null, "You are about to encrypt and send this message", "Confirm",
        JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
    if (response == JOptionPane.NO_OPTION) {
    } else if (response == JOptionPane.YES_OPTION) {
        JOptionPane.showMessageDialog(this, 
                        "Message encrypted. Now sending...",
                        "Done", JOptionPane.INFORMATION_MESSAGE);
                Properties smtpProperties = configUtil.loadProperties();
                EmailUtility.sendEmail(smtpProperties, toAddress, subject, message, null);
			JOptionPane.showMessageDialog(this, 
					"The e-mail has been sent successfully!");
    } else if (response == JOptionPane.CLOSED_OPTION) {
    }
			
		} catch (Exception ex) {
			JOptionPane.showMessageDialog(this, 
					"Error while sending the e-mail: " + ex.getMessage(),
					"Error", JOptionPane.ERROR_MESSAGE);
		}
    }//GEN-LAST:event_jBtSendActionPerformed

