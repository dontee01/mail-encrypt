    private boolean validateFields() {
            if (jTxtTo.getText().equals("")) {
                    JOptionPane.showMessageDialog(this, 
                                    "Please enter To address!",
                                    "Error", JOptionPane.ERROR_MESSAGE);
                    jTxtTo.requestFocus();
                    return false;
            }

            if (jTxtSubj.getText().equals("")) {
                    JOptionPane.showMessageDialog(this, 
                                    "Please enter subject!",
                                    "Error", JOptionPane.ERROR_MESSAGE);
                    jTxtSubj.requestFocus();
                    return false;
            }

            if (jTextAreaMsg.getText().equals("")) {
                    JOptionPane.showMessageDialog(this, 
                                    "Please enter message!",
                                    "Error", JOptionPane.ERROR_MESSAGE);
                    jTextAreaMsg.requestFocus();
                    return false;
            }

            return true;
    }

