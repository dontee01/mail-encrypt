    private void jBtEncryptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtEncryptActionPerformed
        // TODO add your handling code here:
        try {
            String message = jTextAreaMsg.getText().toString();
            if (message.isEmpty())
            {
                JOptionPane.showMessageDialog(this, 
                                "The mail body cannot be empty",
                                "Error", JOptionPane.ERROR_MESSAGE);
            }else
            {
                encryptUtil.Encrypt(message);
                JOptionPane.showMessageDialog(this, 
                                encryptUtil.Encrypt(message),
                                "Done", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                            "Error while encrypting the e-mail: " + e.getMessage(),
                            "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jBtEncryptActionPerformed

